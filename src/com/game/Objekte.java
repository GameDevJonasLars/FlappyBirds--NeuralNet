package com.game;

public class Objekte {
private int iIndex;
private String sName;
public Objekte(int iIndex, String sName) {
	this.iIndex = iIndex;
	this.sName = sName;
}
public int getiIndex() {
	return iIndex;
}
public void setiIndex(int iIndex) {
	this.iIndex = iIndex;
}
public String getsName() {
	return sName;
}
public void setsName(String sName) {
	this.sName = sName;
}

}
